#include "fillit.h"

char	**grow(char **board)
{
	int i;
	int j;

	i = -1;
	while (++i < g_size)
		board[i][g_size] = '.';
	j = -1;
	while (++j <= g_size)
		board[i][j] = '.';
	g_size++;
	return (board);
}
